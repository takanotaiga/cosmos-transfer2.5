from dataclasses import dataclass

from vqa.check import VQACheck


@dataclass
class VQAResult:
    """
    Represents the result of a VQA check.

    Attributes:
        check: The original VQACheck object
        actual_answer: The actual answer generated by the model
        validation_passed: Whether the answer contains at least one required keyword
        found_keywords: List of keywords that were found in the answer
    """

    check: VQACheck
    actual_answer: str
    validation_passed: bool
    found_keywords: list[str]
